def quadratic_model(a, b, c, x):
    return a * x**2 + b * x + c

temperature_coeffs = [float(input("Enter coefficient a for Temperature: ")),
                      float(input("Enter coefficient b for Temperature: ")),
                      float(input("Enter coefficient c for Temperature: "))]

rainfall_coeffs = [float(input("Enter coefficient a for Rainfall: ")),
                   float(input("Enter coefficient b for Rainfall: ")),
                   float(input("Enter coefficient c for Rainfall: "))]

humidity_coeffs = [float(input("Enter coefficient a for Humidity: ")),
                   float(input("Enter coefficient b for Humidity: ")),
                   float(input("Enter coefficient c for Humidity: "))]

days = [1, 100, 200, 365]

temperature = [quadratic_model(*temperature_coeffs, day) for day in days]
rainfall = [quadratic_model(*rainfall_coeffs, day) for day in days]
humidity = [quadratic_model(*humidity_coeffs, day) for day in days]

print("Day\tTemperature\tRainfall\tHumidity")
for day, temp, rain, hum in zip(days, temperature, rainfall, humidity):
    print(f"{day}\t{temp:.2f}\t\t{rain:.2f}\t\t{hum:.2f}")